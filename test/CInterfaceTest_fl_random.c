/* ── main ─────────────────────────────────────────────────────────────────── */

int main(void)
{
  int nfail = 0;

  printf("=== Facility Location Random Instance Tests ===\n");
  printf("    %d UFL + %d CFLP instances, two formulations each\n\n",
         N_UFL_INSTANCES, N_CFLP_INSTANCES);

  for (int k = 0; k < N_UFL_INSTANCES; k++) {
    const UflInstance *inst = &UFL_INSTANCES[k];
    printf("[UFL %d/%d] m=%d n=%d opt=%d\n",
           k + 1, N_UFL_INSTANCES, inst->m, inst->n, inst->opt);
    if (!test_ufl_individual(inst))  nfail++;
    if (!test_ufl_aggregated(inst))  nfail++;
  }

  printf("\n");

  for (int k = 0; k < N_CFLP_INSTANCES; k++) {
    const CflpInstance *inst = &CFLP_INSTANCES[k];
    printf("[CFLP %d/%d] m=%d n=%d opt=%d\n",
           k + 1, N_CFLP_INSTANCES, inst->m, inst->n, inst->opt);
    if (!test_cflp_standard(inst))  nfail++;
    if (!test_cflp_cap_only(inst))  nfail++;
  }

  printf("\n");
  if (nfail == 0)
    printf("=== All facility location tests PASSED ===\n");
  else
    printf("=== FAILED: %d test(s) failed ===\n", nfail);

  return nfail > 0 ? 1 : 0;
}

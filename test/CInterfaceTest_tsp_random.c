int main(void)
{
  printf("=== TSP Random Instance Tests ===\n");
  printf("    %d instances, two formulations each (MTZ + subtour elimination)\n\n",
    N_TSP_INSTANCES);

  for (int i = 0; i < N_TSP_INSTANCES; i++) {
    const TspInstance *inst = &TSP_INSTANCES[i];
    printf("[%d/%d] n=%d seed=%d opt=%d\n",
      i + 1, N_TSP_INSTANCES, inst->n, inst->seed, inst->opt);
    if (inst->n <= 15) /* MTZ LP relaxation is weak for large n; skip n=20 */
      test_mtz(inst);
    test_subtour(inst);
    printf("\n");
  }

  printf("=== All TSP random tests PASSED ===\n");
  return 0;
}
